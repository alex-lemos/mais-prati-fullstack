// Exemplo de laço for
for (let i = 0; i < 5; i++) {
    console.log(i)
}

// Exemplo de laço while
let j = 0;
while (j < 5) {
    console.log(j);
    j++;
}

// Exemplo de laço do-while
let k = 0;
do {
    console.log(k);
    k++;
} while (k < 5);
